// - Getting started with objects

// - key value pair
let item = {
  name:'smartphone',
  price:50000,
  quantity:1
}
console.log(item)

// output -{name: 'smartphone', price: 50000, quantity: 1}

// Another way to create object

let item2 = new Object();
item2.name = "iphone"
item2.price = 90000
item2.quantity = 1
console.log(item2)

// output - {name: 'iphone', price: 90000, quantity: 1}